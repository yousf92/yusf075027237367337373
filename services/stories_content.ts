const fillTo400 = (arr: string[]): string[] => {
  const result: string[] = [];
  const baseLength = arr.length;
  if (baseLength === 0) return Array(400).fill("لا يوجد محتوى");
  for (let i = 0; i < 400; i++) {
    result.push(arr[i % baseLength]);
  }
  return result;
}

